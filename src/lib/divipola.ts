// Función helper para buscar departamentos
export const getDepartmentById = (id: string): Department | undefined => {
  return DIVIPOLA_DATA.find(dept => dept.id === id);
};

// Función helper para buscar municipios
export const getMunicipalityById = (deptId: string, munId: string): Municipality | undefined => {
  const department = getDepartmentById(deptId);
  return department?.municipalities.find(mun => mun.id === munId);
};

// Función para buscar por nombre
export const searchMunicipalities = (searchTerm: string): {department: Department, municipality: Municipality}[] => {
  const results: {department: Department, municipality: Municipality}[] = [];
  
  DIVIPOLA_DATA.forEach(department => {
    department.municipalities.forEach(municipality => {
      if (municipality.name.toLowerCase().includes(searchTerm.toLowerCase())) {
        results.push({ department, municipality });
      }
    });
  });
  
  return results;
};